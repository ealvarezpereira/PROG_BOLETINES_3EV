    private void tBonolotoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tBonolotoMouseClicked
        try {
            Point punto = this.tBonoloto.getMousePosition();

            int fila = this.tBonoloto.rowAtPoint(punto);
            int columna = this.tBonoloto.columnAtPoint(punto);

            numero = (String) this.tBonoloto.getValueAt(fila, columna);

            numeroSeleccionado[i] = numero;

            completo = completo + numero + " ";

            nselec.setText(completo);
            i++;

            if (i == 6) {

                numerosMaquina();
            }

        } catch (ArrayIndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "Ya has seleccionado 6 n√∫meros.");
        }
    }//GEN-LAST:event_tBonolotoMouseClicked

    private void randomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_randomActionPerformed

        for (int j = 0; j < numeroSeleccionado.length; j++) {
            numRandom = (int) (Math.random() * (50) + 1);
            numeroSeleccionado[j] = String.valueOf(numRandom);

        }
        numerosMaquina();
    }//GEN-LAST:event_randomActionPerformed

