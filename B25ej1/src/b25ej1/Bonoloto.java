    public void numerosMaquina() {

        for (int j = 0; j < numeroSeleccionado.length; j++) {
            numRandom = (int) (Math.random() * (50) + 1);
            nRandom[j] = String.valueOf(numRandom);

        }
        comparar();
    }

    public void comparar() {
        int aciertos = 0;

        for (int j = 0; j < nRandom.length; j++) {
            System.out.println("Tu numero: " + numeroSeleccionado[j]);
            System.out.println("Premiado: " + nRandom[j]);
            for (int k = 0; k < numeroSeleccionado.length; k++) {
                if (nRandom[j].equals(numeroSeleccionado[k])) {
                    aciertos++;
                }
            }
        }

        System.out.println("Numero de aciertos: " + aciertos);

        nselec.setText("");
        completo = "";
        numero = "";
        i = 0;
    }

